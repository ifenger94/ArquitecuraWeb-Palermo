class Persona {
    constructor(id,nombre, apellido, edad,deudaVencida,deudaAvencer) {
        this.id = id
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;
        this.deudaVencida = deudaVencida;
        this.deudaAvencer = deudaAvencer;
    }
}

export default Persona;